# 1.	В диапазоне натуральных чисел от 2 до 99 определить,
# сколько из них кратны каждому из чисел в диапазоне от 2 до 9.
list_res = []
for i in range(2, 100):
    list_temp = [i for j in range(2, 9) if i % j == 0]
    if len(list_temp) == 8:
        list_res.append(list_temp)
print(f'В диапазоне натуральных чисел от 2 до 99, кратны каждому из чисел в диапазоне от 2 до 9 = {len(list_res)}')
